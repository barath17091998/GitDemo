# CREATING A CLASS
class calculator:
    num=100
    # DECLARING A METHOD INSIDE A CLASS
    def getData(self):
        print("I am now executing as method in class")

# SYNTAX TO CREATE OBJECTS IN PYTHON
obj=calculator()
# CALLING A FUNCTION INSIDE CLASS USING OBJECT
obj.getData()
# CALLING A VARIABLE INSIDE CLASS USING OBJECT
print(obj.num)



